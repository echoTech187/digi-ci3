        <h4 class="font-weight-bold mb-4 mt-5 border-bottom pb-2">Common Login Issues</h4>
        
        <div class="hc-faq-item">
            <div class="hc-faq-q">
                <i class="fas fa-question-circle text-warning"></i> 
                <span>Issue: "Incorrect username or password"</span>
            </div>
            <div class="hc-faq-a">
                <strong>Symptom:</strong> You are unable to log in and receive an incorrect credentials error.<br><br>
                <strong>Resolution:</strong> Double-check for typos, caps lock, or trailing spaces. If the issue persists, contact your Super Admin to reset your password.
            </div>
        </div>

        <div class="hc-faq-item">
            <div class="hc-faq-q">
                <i class="fas fa-question-circle text-warning"></i> 
                <span>Issue: Account is "Not Active"</span>
            </div>
            <div class="hc-faq-a">
                <strong>Symptom:</strong> The system rejects your login stating your account is not active.<br><br>
                <strong>Resolution:</strong> Your account has been disabled or suspended by an administrator. Contact your Super Admin to reactivate it.
            </div>
        </div>

        <div class="hc-faq-item">
            <div class="hc-faq-q">
                <i class="fas fa-question-circle text-warning"></i> 
                <span>Issue: Immediately logged out after logging in</span>
            </div>
            <div class="hc-faq-a">
                <strong>Symptom:</strong> You are redirected back to the login screen immediately after a successful login.<br><br>
                <strong>Resolution:</strong> Your session was forcefully terminated. This typically happens if another person logs into the same account simultaneously, triggering the single-session security protocol.
            </div>
